#include "../includes/ft_printf.h"

int		int_len(int a)
{
	int len;

	len = 0;
	if (a == -2147483648)
		return (11);
	if (a < 0)
		len++;
	a = if_negative(a);
	if (a == 0)
		return (1);
	while (a > 0)
	{
		a = a / 10;
		len++;
	}
	return (len);
}
